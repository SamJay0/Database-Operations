    private void executeBtnnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_executeBtnnActionPerformed
 
            String query = sqlCommandWindow.getText();
            if (query.contains("select")) {
                //execte select query
                ResultSet rslt = db_connect.selectQuery(query,resultPanel,resultTableContainer);
                resultTable.setModel(DbUtils.resultSetToTableModel(rslt));
                resultTableContainer.setVisible(true);

            } else {
                //insert , delete ,and update operations
                db_connect.insertDeleteUpdateQuery(query,resultPanel,resultTableContainer);
            }

      


    }//GEN-LAST:event_executeBtnnActionPerformed

