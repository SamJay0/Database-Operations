    private void connectToDatabaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_connectToDatabaseActionPerformed
        String driver, url, user, password;
        driver = driverCombo.getSelectedItem().toString();
        url = UrlCombo.getSelectedItem().toString();
        user = userTxt.getText();
        password = String.valueOf(passwordTxt.getPassword());
        Connection con = db_connect.createConnection(driver, url, user, password,resultWindow);
        if (con != null) {
            connectionMsgTxt.setText("connected to " + url);
        } else {
            connectionMsgTxt.setText("No connection yet");
        }


    }//GEN-LAST:event_connectToDatabaseActionPerformed

